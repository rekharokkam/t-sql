/*
	Transaction is a logical Unit of work
	
	Transaction Management means ensuring that a set of SQL statements is treated as a UNIT
	
	Transaction Management guarantees that either all operations are completed or none is complete.
	
	COMMIT - commit permanently saves all the changes made to the database since the begining of a transaction.
	
	ROLLBACK - undoes all changes made to the database since the begining of a transaction. 
*/