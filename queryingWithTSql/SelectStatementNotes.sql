/*
	Select statement provides the ability to retrieve data from database
	
	Select statement is made of 6 main parts / Clauses
	
		SELECT - Identifies the columns to be included in the results set 
		FROM - Identifies the tables that will be queried
		WHERE - Limits the number of rows returned in the result set
		GROUP BY - Performs grouping action on the results set
		HAVING - Specifies the search condition for a group
		ORDER BY - Determines the order of the results set		
		
	Any number of white spaces does not matter in select query
*/